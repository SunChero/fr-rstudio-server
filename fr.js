var insertionListener = function(event) {
	   // Making sure that this is the animation we want.
	if (event.animationName === "nodeInserted") {
	    // event.target.innerText != "" ? event.target.innerText = fetchTerm(event.target.innerText) : null
	    var term = event.target.innerText
	    var ascii = /^[ -~]+$/;
	   if ( !ascii.test( term ) ) {
	        return
	   }
	   event.target.innerText = fr(term)
	}
}
